package week5_6;

import java.awt.*;

class Rectangle extends Shape {
    private double length, width;

    public Rectangle(double _x1, double _y1, double length, double width) {
        super(_x1, _y1);
        this.length = length;
        this.width = width;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double getwidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    @Override
    public String toString() {
        return "Rectangle{" +
                "length=" + length +
                ", width=" + width +
                '}';
    }
}
